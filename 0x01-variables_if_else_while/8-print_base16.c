/**
 * main -Entry point
 * Description: this is where the programme
 * first starts executing
 *
 * Return: Return (0) Always (Success)
 */
int main(void)
{
	char i;

	int j;

	i = 'a';
	j = 0;

	while 
		(j < 10) {
		putchar(j + '0');
		j++;
		}
	while
		(i <= 'f') {
			putchar(i);
			i++;
		}
	putchar('\n');
	return (0);
}
