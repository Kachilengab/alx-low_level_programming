#include <stdio.h>

/** more headers goes there*/
/**
 * main -Entry point
 * Description: this is where the programme
 * first starts executing
 *
 * Return: Return (0) Always (Success)
 */
int main(void)
{
	char i;

	i = 'a';
	while
		(i <= 'z') {
			if ((i != 'q' && i != 'e') && i <= 'z')
				putchar(i);
			i++;
		}
	putchar('\n');
	return (0);
}
